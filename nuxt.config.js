const pkg = require('./package');
import axios from 'axios'

let dynamicRoutes = () => {
  return axios.get('https://fast-reef-73314.herokuapp.com/recipes')
  .then(res => {
    return res.data.map(recipe => {
      return {
        route: `/recipes/ + ${recipe.id}`,
        payload: recipe
      }
    })
  })
}

